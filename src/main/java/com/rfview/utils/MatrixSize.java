package com.rfview.utils;

public class MatrixSize {

    private final int sizeInputs;
    private final int sizeOutputs;

    public int getSizeInputs() {
        return sizeInputs;
    }

    public int getSizeOutputs() {
        return sizeOutputs;
    }

    public MatrixSize(int sizeInputs, int sizeOutputs) {
        super();
        this.sizeInputs = sizeInputs;
        this.sizeOutputs = sizeOutputs;
    }

}
